void PrintImage(int[,] image)
{
for (int i = 0; i < image.GetLength(0); i++)
{
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" ");
            else if (image[i, j] == 2) Console.Write($"'");
            else Console.Write($"*");
        }
        Console.WriteLine();
    }
}

void ColorizeImage(int row, int col) // аргументы - позиция строки и пикселя, с которого начинаем обход
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 2;
        ColorizeImage(row - 1, col);
        ColorizeImage(row, col - 1);
        ColorizeImage(row + 1, col);
        ColorizeImage(row, col + 1);
    }
}